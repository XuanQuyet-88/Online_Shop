                SectionTitle("Most popular", "See All")
            }

            //List item popular
            item {
                if(showPopularLoading){
                    Box(
                        modifier = Modifier
                            .fillMaxWidth()
                            .height(200.dp),
                        contentAlignment = Alignment.Center
                    ){
                        CircularProgressIndicator()
                    }
                }else{
                    ListItems(popularState.value)
                }
            }
        }
    }
